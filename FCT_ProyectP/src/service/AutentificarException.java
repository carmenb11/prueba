package service;

public class AutentificarException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3558034243090041856L;

	public AutentificarException() {
	}

	public AutentificarException(String message) {
		super(message);

	}

	public AutentificarException(Throwable cause) {
		super(cause);

	}

	public AutentificarException(String message, Throwable cause) {
		super(message, cause);

	}

	public AutentificarException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
